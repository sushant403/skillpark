     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show(Job $job)
    {
        $job->load('categories');

        if (!auth()->check()) {
            return view('services.service-single', compact('job'));
        } else {
            $user = Auth::user();

            if ($user->hasRole('freelancer')) {
                return view('jobs.job-details', compact('job'));
            }
            abort(404);
        }
    }

